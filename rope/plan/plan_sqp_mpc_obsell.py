def preprocess_pixels(
    pixels: np.ndarray | torch.Tensor,
    *,
    img_size: int,
    pixel_mean: torch.Tensor,
    pixel_std: torch.Tensor,
) -> torch.Tensor:
    if isinstance(pixels, np.ndarray):
        tensor = torch.from_numpy(np.ascontiguousarray(pixels))
    else:
        tensor = pixels
    if tensor.ndim == 3:
        tensor = tensor.unsqueeze(0)
    tensor = tensor.permute(0, 3, 1, 2).float().div_(255.0)
    if tuple(tensor.shape[-2:]) != (img_size, img_size):
        tensor = torch.nn.functional.interpolate(
            tensor,
            size=(img_size, img_size),
            mode="bilinear",
            align_corners=False,
        )
    return (tensor - pixel_mean) / pixel_std


@torch.no_grad()
def encode_frames(
    model: torch.nn.Module,
    pixels: torch.Tensor,
    *,
    device: torch.device,
    frame_batch_size: int,
) -> torch.Tensor:
    latents = []
    for start in range(0, pixels.shape[0], frame_batch_size):
        chunk = pixels[start : start + frame_batch_size].to(device)
        output = model.encoder(chunk, interpolate_pos_encoding=True)
        emb = model.projector(output.last_hidden_state[:, 0])
        latents.append(emb)
    return torch.cat(latents, dim=0)


@torch.no_grad()
def encode_single_frame(
    model: torch.nn.Module,
    pixel: np.ndarray,
    *,
    device: torch.device,
    img_size: int,
    pixel_mean: torch.Tensor,
    pixel_std: torch.Tensor,
) -> torch.Tensor:
    batch = preprocess_pixels(pixel, img_size=img_size, pixel_mean=pixel_mean, pixel_std=pixel_std).to(device)
    output = model.encoder(batch, interpolate_pos_encoding=True)
    return model.projector(output.last_hidden_state[:, 0])[0]


def make_markov_state(history: list[torch.Tensor], markov_deriv: int) -> torch.Tensor:
    context_len = required_markov_history(markov_deriv)
    if not history:
        raise ValueError("At least one embedding is required to build the Markov state.")
    history_tensor = torch.stack(history[-context_len:], dim=0)
    if history_tensor.shape[0] < context_len:
        pad = history_tensor[:1].repeat(context_len - history_tensor.shape[0], 1)
        history_tensor = torch.cat((pad, history_tensor), dim=0)
    return build_markov_state(history_tensor, markov_deriv)


def normalized_to_raw_action(action_norm: np.ndarray, action_mean: np.ndarray, action_std: np.ndarray) -> np.ndarray:
    return (action_norm * action_std.reshape(-1) + action_mean.reshape(-1)).astype(np.float32)


def metadata_array(metadata: dict[str, object], key: str) -> np.ndarray:
    if key not in metadata:
        raise KeyError(f"Obstacle model metadata is missing {key!r}; regenerate the obstacle model artifact.")
    value = np.asarray(metadata[key], dtype=np.float64)
    if value.shape != (3,):
        raise ValueError(f"Expected obstacle metadata {key!r} with shape (3,), got {value.shape}.")
    return value


def endpoint_boxes_from_metadata(metadata: dict[str, object]) -> list[dict[str, object]]:
    raw_boxes = metadata.get("endpoint_sampling_boxes")
    if raw_boxes is None:
        lower = metadata_array(metadata, "task_lower")
        upper = metadata_array(metadata, "task_upper")
        obstacle_reach_arr = np.asarray(metadata.get("obstacle_reach"), dtype=np.float64)
        if obstacle_reach_arr.shape != (2,):
            raise ValueError("Obstacle model metadata must contain obstacle_reach with shape (2,).")
        raw_boxes = [
            {
                "name": "reach_below_obstacle",
                "lower": lower,
                "upper": np.array([obstacle_reach_arr[0], upper[1], upper[2]], dtype=np.float64),
            },
            {
                "name": "reach_above_obstacle",
                "lower": np.array([obstacle_reach_arr[1], lower[1], lower[2]], dtype=np.float64),
                "upper": upper,
            },
        ]

    boxes: list[dict[str, object]] = []
    for index, raw_box in enumerate(raw_boxes):
        if not isinstance(raw_box, dict):
            raise ValueError(f"Endpoint sampling box {index} is not a dictionary.")
        lower = np.asarray(raw_box.get("lower"), dtype=np.float64)
        upper = np.asarray(raw_box.get("upper"), dtype=np.float64)
        if lower.shape != (3,) or upper.shape != (3,):
            raise ValueError(f"Endpoint sampling box {index} must have lower/upper shape (3,).")
        span = upper - lower
        if np.any(span < 0.0):
            raise ValueError(f"Endpoint sampling box {index} has upper below lower.")
        volume = float(np.prod(span))
        if volume <= 0.0:
            continue
        boxes.append(
            {
                "name": str(raw_box.get("name", f"box_{index}")),
                "lower": lower,
                "upper": upper,
                "volume": volume,
            }
        )
    if not boxes:
        raise ValueError("Obstacle metadata did not provide any non-empty endpoint sampling boxes.")
    return boxes


def sample_task_state_from_box(rng: np.random.Generator, box: dict[str, object]) -> np.ndarray:
    lower = np.asarray(box["lower"], dtype=np.float64)
    upper = np.asarray(box["upper"], dtype=np.float64)
    return rng.uniform(lower, upper).astype(np.float32)


def endpoint_crossing_boxes(boxes: list[dict[str, object]]) -> tuple[dict[str, object], dict[str, object]]:
    if len(boxes) < 2:
        raise ValueError("Forced obstacle crossing requires endpoint sampling boxes on both sides of the obstacle.")
    sorted_boxes = sorted(
        boxes,
        key=lambda box: float(0.5 * (np.asarray(box["lower"], dtype=np.float64)[0] + np.asarray(box["upper"], dtype=np.float64)[0])),
    )
    low_reach_box = sorted_boxes[0]
    high_reach_box = sorted_boxes[-1]
    if str(low_reach_box["name"]) == str(high_reach_box["name"]):
        raise ValueError("Forced obstacle crossing could not identify distinct low/high reach endpoint boxes.")
    return low_reach_box, high_reach_box


def sample_safe_task_endpoints(
    obstacle: ConformalEllipsoidObstacleConstraint,
    rng: np.random.Generator,
    *,
    min_distance: float,
    max_attempts: int,
) -> tuple[np.ndarray, np.ndarray, dict[str, object]]:
    metadata = obstacle.source_metadata
    if not isinstance(metadata, dict):
        raise ValueError("Obstacle model source metadata is not a dictionary; regenerate the obstacle model artifact.")
    boxes = endpoint_boxes_from_metadata(metadata)

    low_reach_box, high_reach_box = endpoint_crossing_boxes(boxes)
    start_box = low_reach_box
    goal_box = high_reach_box
    if bool(rng.integers(0, 2)):
        start_box, goal_box = goal_box, start_box
    start = sample_task_state_from_box(rng, start_box)
    goal = sample_task_state_from_box(rng, goal_box)
    attempts = 1
    while float(np.linalg.norm(goal - start)) < float(min_distance):
        if attempts >= int(max_attempts):
            raise RuntimeError("Could not sample safe start/goal endpoints separated by the requested distance.")
        start = sample_task_state_from_box(rng, start_box)
        goal = sample_task_state_from_box(rng, goal_box)
        attempts += 1

    sampling_info: dict[str, object] = {
        "rule": "force obstacle crossing by sampling start and goal on opposite sides of the obstacle reach interval",
        "boxes": [
            {
                "name": str(box["name"]),
                "lower": np.asarray(box["lower"], dtype=np.float32).tolist(),
                "upper": np.asarray(box["upper"], dtype=np.float32).tolist(),
                "volume": float(box["volume"]),
            }
            for box in boxes
        ],
        "start_box": str(start_box["name"]),
        "goal_box": str(goal_box["name"]),
        "min_start_goal_distance": float(min_distance),
        "goal_resample_attempts": int(attempts),
    }
    return start, goal, sampling_info


def proxy_rope_low_heights_for_task_states(task_states: np.ndarray) -> np.ndarray:
    proxy_env = LabEnv(base_config=BaseEnvConfig(enable_proxy_rope=True))
    states = np.asarray(task_states, dtype=np.float64)
    low_heights = np.zeros((states.shape[0],), dtype=np.float64)
    for index, state in enumerate(states):
        proxy_env.reset(TaskState.from_array(state))
        points = proxy_env.get_proxy_rope_points()
        low_heights[index] = float(np.min(points[:, 2]))
    return low_heights


def save_planner_proxy_rope_diagnostic(
    out_path: Path,
    task_states: np.ndarray,
    low_rope_height: np.ndarray,
    *,
    goal_task_state: np.ndarray,
    goal_low_rope_height: float,
    task_reach_bounds: tuple[float, float] | None,
    obstacle_reach: tuple[float, float] | None,
    low_rope_cutoff: float | None,
) -> None:
    states = np.asarray(task_states, dtype=np.float64)
    heights = np.asarray(low_rope_height, dtype=np.float64)
    goal_state = np.asarray(goal_task_state, dtype=np.float64)
    goal_height = float(goal_low_rope_height)
    steps = np.arange(states.shape[0], dtype=np.int64)
    fig, ax = plt.subplots(figsize=(7.5, 7.0), dpi=180)
    y_values = [float(np.min(heights)), float(np.max(heights)), goal_height]
    if low_rope_cutoff is not None:
        y_values.append(float(low_rope_cutoff))
    y_min = min(y_values)
    y_max = max(y_values)
    y_center = 0.5 * (y_min + y_max)
    y_half_range = max(0.5 * (y_max - y_min) * 1.25, 0.075)
    y_lower = y_center - y_half_range
    y_upper = y_center + y_half_range
    if obstacle_reach is not None and low_rope_cutoff is not None:
        ax.fill_between(
            [obstacle_reach[0], obstacle_reach[1]],
            y_lower,
            float(low_rope_cutoff),
            color="#d55e00",
            alpha=0.08,
            label="obstacle region",
        )
    elif obstacle_reach is not None:
        ax.axvspan(obstacle_reach[0], obstacle_reach[1], color="#d55e00", alpha=0.08, label="obstacle reach interval")
    if low_rope_cutoff is not None:
        ax.axhline(low_rope_cutoff, color="#4d4d4d", linestyle="--", linewidth=1.2, label="clearance threshold")
    ax.plot(states[:, 0], heights, color="#4d4d4d", linewidth=1.0, alpha=0.55, zorder=1)
    scatter = ax.scatter(
        states[:, 0],
        heights,
        c=steps,
        cmap="viridis",
        s=34.0,
        alpha=0.9,
        edgecolors="white",
        linewidths=0.35,
        label="executed planner states",
        zorder=2,
    )
    ax.scatter(states[0, 0], heights[0], marker="o", s=72.0, c="#0072b2", edgecolors="black", linewidths=0.5, label="start")
    ax.scatter(
        goal_state[0],
        goal_height,
        marker="D",
        s=86.0,
        c="#009e73",
        edgecolors="black",
        linewidths=0.5,
        label="goal",
        zorder=4,
    )
    ax.scatter(states[-1, 0], heights[-1], marker="*", s=120.0, c="#cc79a7", edgecolors="black", linewidths=0.5, label="final")
    cbar = fig.colorbar(scatter, ax=ax)
    cbar.set_label("executed step")
    ax.set_title("Planner trajectory by reach and proxy rope low height")
    ax.set_xlabel("reach")
    ax.set_ylabel("proxy rope low height")
    if task_reach_bounds is not None:
        ax.set_xlim(task_reach_bounds[0], task_reach_bounds[1])
    ax.set_ylim(y_lower, y_upper)
    ax.grid(alpha=0.2)
    ax.legend(loc="best")
    fig.tight_layout()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out_path)
    plt.close(fig)


