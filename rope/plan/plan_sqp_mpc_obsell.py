def load_model(checkpoint_path: Path, device: torch.device) -> torch.nn.Module:
    if not checkpoint_path.is_file():
        raise FileNotFoundError(f"Checkpoint not found: {checkpoint_path}")
    model = torch.load(checkpoint_path, map_location=device, weights_only=False)
    model = model.to(device)
    model.eval()
    model.requires_grad_(False)
    return model


def load_numpy_artifact(path: Path) -> dict[str, object]:
    try:
        return torch.load(path, map_location="cpu", weights_only=False)
    except ModuleNotFoundError as exc:
        if exc.name != "numpy._core":
            raise

    import numpy.core.multiarray as numpy_core_multiarray

    numpy_core_multiarray._reconstruct.__module__ = "numpy._core.multiarray"
    numpy_core = types.ModuleType("numpy._core")
    numpy_core.multiarray = numpy_core_multiarray
    sys.modules.setdefault("numpy._core", numpy_core)
    sys.modules.setdefault("numpy._core.multiarray", numpy_core_multiarray)
    safe_globals = [
        numpy_core_multiarray._reconstruct,
        np.ndarray,
        np.dtype,
        type(np.dtype("float64")),
        type(np.dtype("float32")),
        type(np.dtype("int64")),
        type(np.dtype("bool")),
    ]
    with torch.serialization.safe_globals(safe_globals):
        return torch.load(path, map_location="cpu", weights_only=True)


